package Tarifa;

public class Tarifa {

    private int cantidad;

    //------------------------------------------------------------------
    // CONSTRUCTORES
    //------------------------------------------------------------------

    public Tarifa(){
        this.cantidad = 0;
    }

    public Tarifa(int cantidad){
        this.cantidad = cantidad;
    }

    //------------------------------------------------------------------
    // METODOS DE USO
    //------------------------------------------------------------------





    //------------------------------------------------------------------
    // GETTERS
    //------------------------------------------------------------------

    public int getCantidad() {
        return cantidad;
    }


    //------------------------------------------------------------------
    // SETTERS
    //------------------------------------------------------------------

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
}
